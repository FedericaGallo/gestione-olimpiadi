package Entity;

public class Olimpiade {
    private int id;
    private int anno;
    private String paeseOspitante;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getAnno() {
        return anno;
    }

    public void setAnno(int anno) {
        this.anno = anno;
    }

    public String getPaeseOspitante() {
        return paeseOspitante;
    }

    public void setPaeseOspitante(String paeseOspitante) {
        this.paeseOspitante = paeseOspitante;
    }
}

